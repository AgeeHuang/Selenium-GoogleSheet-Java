package ageeweb.test.web.sheet;

public class CarInfo {
  
  /** 車輛數. */
  private String carAmount;
  
  /** 取車日期. */
  private String carDate;
  
  /** 取車時間. */
  private String carTime;
  
  /** 取車站. */
  private String carStation;
  
  /** 時數. */
  private String carHrs;
  
  /** 還車日期. */
  private String carBackDate;

  /** 還車時間. */
  private String carBackTime;

  
  public String getCarAmount() {
    return carAmount;
  }

  public void setCarAmount(String carAmount) {
    this.carAmount = carAmount;
  }

  public String getCarDate() {
    return carDate;
  }

  public void setCarDate(String carDate) {
    this.carDate = carDate;
  }

  public String getCarTime() {
    return carTime;
  }

  public void setCarTime(String carTime) {
    this.carTime = carTime;
  }

  public String getCarStation() {
    return carStation;
  }

  public void setCarStation(String carStation) {
    this.carStation = carStation;
  }

  public String getCarHrs() {
    return carHrs;
  }

  public void setCarHrs(String carHrs) {
    this.carHrs = carHrs;
  }

  public String getCarBackDate() {
    return carBackDate;
  }

  public void setCarBackDate(String carBackDate) {
    this.carBackDate = carBackDate;
  }
  
  public String getCarBackTime() {
    return carBackTime;
  }

  public void setCarBackTime(String carBackTime) {
    this.carBackTime = carBackTime;
  }
}
